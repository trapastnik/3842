def L(id, name_ru, endonym, writing, script, family, terr, dia,
      primary, also, speakers, weight, source="pdf-specimen",
      verifier="machine-triangulated", note=None):
    e = {
        "id": id, "name_ru": name_ru, "endonym": endonym, "writing": writing,
        "script": {"iso15924": script, "name_ru": SCRIPTS[script]},
        "family": family,
        "geo": {"territorial": terr, "diaspora": dia,
                "primary": primary, "also": also or []},
        "writing_source": source, "verifier": verifier,
        "speakers_mln": speakers, "weight": weight,
    }
    if note:
        e["note"] = note
    return e

